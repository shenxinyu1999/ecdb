	public void doMarkDup() {
		Connection c = dbcs.getConnection();
		String query = "DECLARE @Temp TABLE (  \r\n" + 
				"	[编号] [int] NULL,\r\n" + 
				"	[运单编号] [varchar](8000) NULL,\r\n" + 
				"	[收件省] [varchar](8000) NULL,\r\n" + 
				"	[重量] [float] NULL,\r\n" + 
				"	[计费重量] [float] NULL,\r\n" + 
				"	[单位总价] [float] NULL,\r\n" + 
				"	[快递公司] [varchar](8000) NULL,\r\n" + 
				"	[月份] [int] NULL,\r\n" + 
				"	[备注] [varchar](8000) NULL\r\n" + 
				") \r\n" + 
				"\r\n" + 
				"INSERT INTO @Temp SELECT 原始数据.* \r\n" + 
				"FROM 原始数据 JOIN \r\n" + 
				"(SELECT MAX(月份) AS 月份, 运单编号 FROM 原始数据 GROUP BY 运单编号 HAVING COUNT(运单编号) =2) AS k\r\n" + 
				"ON k.月份 = 原始数据.月份 AND k.运单编号 = 原始数据.运单编号\r\n" + 
				"\r\n" + 
				"UPDATE 原始数据\r\n" + 
				"SET 备注 = '重复'\r\n" + 
				"FROM @Temp AS a\r\n" + 
				"WHERE 原始数据.编号 = a.编号 AND\r\n" + 
				"	原始数据.运单编号 = a.运单编号 AND\r\n" + 
				"	原始数据.收件省 = a.收件省 AND\r\n" + 
				"	原始数据.重量 = a.重量 AND\r\n" + 
				"	原始数据.计费重量 = a.计费重量 AND\r\n" + 
				"	原始数据.单位总价 = a.单位总价 AND\r\n" + 
				"	原始数据.快递公司 = a.快递公司 AND\r\n" + 
				"	原始数据.月份 = a.月份 AND\r\n" + 
				"	原始数据.备注 = a.备注\r\n" + 
				"\r\n" + 
				"DELETE FROM @Temp\r\n" + 
				"\r\n" + 
				"INSERT INTO @Temp SELECT 原始数据.* \r\n" + 
				"FROM 原始数据 JOIN \r\n" + 
				"(SELECT MAX(编号) AS 编号, 运单编号 FROM 原始数据 GROUP BY 运单编号,月份 HAVING COUNT(运单编号) =2) AS k\r\n" + 
				"ON k.编号 = 原始数据.编号 AND k.运单编号 = 原始数据.运单编号\r\n" + 
				"\r\n" + 
				"UPDATE 原始数据\r\n" + 
				"SET 备注 = '同月重复'\r\n" + 
				"FROM @Temp AS a\r\n" + 
				"WHERE 原始数据.编号 = a.编号 AND\r\n" + 
				"	原始数据.运单编号 = a.运单编号 AND\r\n" + 
				"	原始数据.收件省 = a.收件省 AND\r\n" + 
				"	原始数据.重量 = a.重量 AND\r\n" + 
				"	原始数据.计费重量 = a.计费重量 AND\r\n" + 
				"	原始数据.单位总价 = a.单位总价 AND\r\n" + 
				"	原始数据.快递公司 = a.快递公司 AND\r\n" + 
				"	原始数据.月份 = a.月份 AND\r\n" + 
				"	原始数据.备注 = a.备注";
		
		ResultSet rs = null;
		try {
			PreparedStatement stmt = c.prepareStatement(query, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
			rs = stmt.executeQuery();
		} catch (SQLException e) {
			if (!e.getMessage().equals("The statement did not return a result set.")) {
				Main.gui.displayMessage(e.getMessage());
			}
		}
	}

	
	public void findOrderNum(int count) {
		Connection c = dbcs.getConnection();
		String query = "SELECT * INTO 结果 FROM 原始数据 WHERE 备注=''\r\n" + 
				"\r\n" + 
				"ALTER TABLE 结果\r\n" + 
				"ADD \r\n" + 
				"对应订单号 varchar(8000),\r\n" + 
				"对应店铺 varchar(8000)\r\n" + 
				"\r\n" + 
				"UPDATE 结果\r\n" + 
				"SET 对应订单号 = ''\r\n" + 
				"WHERE 对应订单号 IS NULL\r\n" + 
				"\r\n" + 
				"UPDATE 结果\r\n" + 
				"SET 对应店铺 = ''\r\n" + 
				"WHERE 对应店铺 IS NULL\r\n";
		for(int i = 1; i<count; i++) {
			String s = "对应数据" + Integer.toString(i);
			query = query + 
					"UPDATE t2\r\n" + 
					"SET t2.对应订单号 = t1.订单号\r\n" + 
					"FROM (SELECT * FROM "
					+ s
					+ " WHERE 电子面单号 IN (SELECT 电子面单号 FROM "
					+ s
					+ " GROUP BY 电子面单号 HAVING COUNT(电子面单号) = 1)) AS t1\r\n" + 
					"JOIN 结果 t2 on t1.电子面单号 = t2.运单编号\r\n" + 
					"WHERE 对应订单号 = ''";
		}
		
				
		
		ResultSet rs = null;
		try {
			PreparedStatement stmt = c.prepareStatement(query, ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
			rs = stmt.executeQuery();
		} catch (SQLException e) {
			Main.gui.displayMessage(e.getMessage());
		}
	}

